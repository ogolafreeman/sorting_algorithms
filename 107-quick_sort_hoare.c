/**
 * swap_ints -...
 * @a: ...
 * @b: ...
 */
void swap_ints(int *a, int *b)
{
	int tmp;

	tmp = *a;
	*a = *b;
	*b = tmp;
}

/**
 * hoare_partition - ...
 * @array: ...
 * @size: ...
 * @left: ...
 * @right: ...
 * Return: ...
 * Description: ...
 */
int hoare_partition(int *array, size_t size, int left, int right)
{
	int driver, up, down;

	driver = array[right];

	for (up = left - 1, down = right + 1; up < down;)
	{
		do {
			up++;
		} while (array[up] < driver);
		do {
			down--;
		} while (array[down] > driver);

		if (up < down)
		{
			swap_ints(array + up, array + down);
			print_array(array, size);
		}
	}

	return (up);
}

/**
 * hoare_sort - ...
 * @array: ...
 * @size: ...
 * @left: ...
 * @right: ...
 * Description: ...
 */
void hoare_sort(int *array, size_t size, int left, int right)
{
	int belong;

	if (right - left > 0)
	{
		belong = hoare_partition(array, size, left, right);

		hoare_sort(array, size, left, belong - 1);

		hoare_sort(array, size, belong, right);
	}
}

/**
 * quick_sort_hoare - ...
 * @array: ...
 * @size: ...
 * Description: ...
 */
void quick_sort_hoare(int *array, size_t size)
{
	if (array == NULL || size < 2)
		return;

	hoare_sort(array, size, 0, size - 1);
}
